class Subway2 {
public:
	double minTime(int, int, int);
};

double Subway2::minTime(int length, int mA, int mV) {

	double v=mV,a=mA;
	double d=mV*mV/mA;
	double t;

	if(d<=length)
	{
		t=2*v/a+(length-2*(v*v/2/a))/v;
	}
	else
	{
		v=sqrt(a*length);
		t=2*(v/a);
	
	}

	
			
	return t;
		
}




