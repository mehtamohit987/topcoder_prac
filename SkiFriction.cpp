class SkiFriction {
public:
	int bestPosition(string, string);
};

int SkiFriction::bestPosition(string s, string p) {//(string skiFriction, string pathFriction) {
	if(s.size()>=p.size()) return 0;
	int n=p.size(),m=s.size(),i,j,fr,t=0;
	for(i=0;i<n-m;i++)
	{
		fr=0;
		for(j=0;j<m;j++)
		{
			fr=max(fr,p[j+i]+s[j]-96);
		}
		t+=fr;
	}
	
	
	return t;
}


