class ColumnDiagramPerimeter {
public:
	int getPerimiter(vector <int>);
};

int ColumnDiagramPerimeter::getPerimiter(vector <int> a) {

	int sum=0;
	for(int i=0;i<a.size();i++)
	{
		if(a[i]>0)
		{
			sum+=2;
			sum+=abs(a[i]-(i-1>=0?a[i-1]:0));
		}
		else
		sum+=1;
		
		
		cout<<sum;
	}
	sum+=a[a.size()-1];

	return sum;	
}


