class MountainRanges {
public:
	int countPeaks(vector <int>);
};

int MountainRanges::countPeaks(vector <int> heights) {
	
	int r=0;
	for(int i=0;i<heights.size();i++)
	if((i>0?heights[i-1]<heights[i]:true)&&(i<heights.size()-1?heights[i+1]<heights[i]:true))
	r++;
	
	return r;
	
}


