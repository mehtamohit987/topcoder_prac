class Towers {
public:
	int attack(int, int, int, int);
};

int Towers::attack(int myUnits, int hpT, int attackT, int numT) {
	
	int m=numT*hpT,round=0;
	while(m>0&&myUnits>0)
	{
		round++;
		m-=myUnits;
		if(m>0)
		myUnits-=((m%hpT>0?m/hpT+1:m/hpT)*attackT);
	
	}
	if(myUnits>0)
	return round;
	else
	return -1;
	
}



