	SkiFriction * obj = new SkiFriction();
	clock_t start = clock();
	int my_answer = obj->bestPosition(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 33;
	cout <<"Desired answer: " <<endl;
