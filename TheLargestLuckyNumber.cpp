class TheLargestLuckyNumber {
public:
	int find(int);
};

int TheLargestLuckyNumber::find(int n) {
	
	int i,j,r;
	bool f;
	for(i=n;i>4;i--)
	{
		j=i;
		f=false;
		while(!f&&j!=0)
		{
			r=j%10;
			j=j/10;
			if(r==4||r==7){}
			else f=true;
		}
		
		if(!f)
		{return i;break;}
	
	}
	return 4;
}


