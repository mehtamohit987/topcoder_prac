class MagicSpell {
public:
	string fixTheSpell(string);
};

string MagicSpell::fixTheSpell(string spell) {

	vector<int> V;
	string C=spell;
	for(int i=0;i<spell.length();i++)
	if(spell[i]=='A'||spell[i]=='Z') V.push_back(i);
	int n=V.size();
	for(int i=0;i<n;i++)
	{
		C[V[n-i-1]]=spell[V[i]];
	
	}
	return C;
}


double test0() {
	string p0 = "AZ";
	MagicSpell * obj = new MagicSpell();
	clock_t start = clock();
	string my_answer = obj->fixTheSpell(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	string p1 = "ZA";
	cout <<"Desired answer: " <<endl;
	cout <<"\t\"" << p1 <<"\"" <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t\"" << my_answer<<"\"" <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
