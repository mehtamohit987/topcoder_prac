class TheBeauty {
public:
	int find(int);
};

int TheBeauty::find(int n) {
	int ans=0,r;
	vector<bool> B(10,false);
	while(n>0)
	{
		r=n%10;
		B[r]=true;
		n/=10;
	}
	
	for(int i=0;i<10;i++)
	if(B[i])ans++;
	return ans;
}


double test0() {
	int p0 = 7;
	TheBeauty * obj = new TheBeauty();
	clock_t start = clock();
	int my_answer = obj->find(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 1;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
