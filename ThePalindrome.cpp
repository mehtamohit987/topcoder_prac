class ThePalindrome {
public:
	int find(string);
};

int ThePalindrome::find(string s) {
	
	int i,n=s.length(),end=n-1,start=0;
	cout<<"\n"<<n<<"\n";
	for(i=0;i<end;i++)
	{

		if(s[i]==s[end]) end--;
		else if(i==end-1)
		{		end--;break;}
		else
		end=n-1;start=i+1;
	}
	cout<<i<<"\n";
	
	if(i==n-1) return (s.length()-1)*2+1;
	else
	{
		if(i==end)
		{
			return i*2+1;
		}
		else
		{
			i--;
			end++;
			
			return 2*(i+1);
		
		
		}
	
	}
	
}


