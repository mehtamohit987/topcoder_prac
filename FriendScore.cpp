class FriendScore {
public:
	int highestScore(vector <string>);
};

int FriendScore::highestScore(vector <string> friends) {
	
	int i,j,k,n=friends.size(),m=friends[0].length();
	vector<int> cn(n,0);
	for(i=0;i<n;i++)
	{
	vector<bool> B(n,false);int cnt=0;
	for(j=0;j<m;j++)
	{
		if(friends[i][j]=='Y')
		{
			if(!B[j]){cout<<j<<"\n";cnt++;B[j]=true;}
			for(k=0;k<n;k++)
			{
				if(k!=i&&friends[j][k]=='Y'&&B[k]==false)	
				{cout<<k<<"\n";B[k]=true;cnt++;}
			
			}
		
		}
	
	}
cout<<(	cn[i]=cnt)<<"\n";
	}
	
	
	return *max_element(cn.begin(),cn.end());
}


