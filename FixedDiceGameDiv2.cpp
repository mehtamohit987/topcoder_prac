class FixedDiceGameDiv2 {
public:
	double getExpectation(int, int);
};

double FixedDiceGameDiv2::getExpectation(int a, int b) {

	 int sum=0,c=0;
    double ans;
    for(int i=1;i<=b;i++)
    for(int j=i+1;j<=a;j++)
    {
        sum+=j;
        c++;
    }
    ans=(double)(sum)/c;
//    cout<<ans;
    return ans;	
}


