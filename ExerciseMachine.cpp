class ExerciseMachine {
public:
	int getPercentages(string);
};

int ExerciseMachine::getPercentages(string time) {
	
    int h=(int)(time[0]-48)*10+(int)(time[1]-48);
    int m=(int)(time[3]-48)*10+(int)(time[4]-48);
    int s=(int)(time[6]-48)*10+(int)(time[7]-48);


    int n=0,S=h*3600+m*60+s;
    float Z;

    for(int x=1;x<100;x++)
    {
        Z=(x*S)*0.01;

        if((Z-floor(Z))==0)
            n++;

    }

    return n;
}


double test0() {
	string p0 = "00:30:00";
	ExerciseMachine * obj = new ExerciseMachine();
	clock_t start = clock();
	int my_answer = obj->getPercentages(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 99;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
