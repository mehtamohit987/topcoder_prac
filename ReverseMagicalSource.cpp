class ReverseMagicalSource {
public:
	int find(int, int);
};

int ReverseMagicalSource::find(int source, int A) {
	int ans=0,p=1,k;
	bool a=true;
	while(a)
	{
		
		k=source*p;
		p*=10;
		if(ans<=A&&ans+k>A)
		a=false;
		
		ans+=k;
	}
	
	return ans;
	
}


double test0() {
	int p0 = 19;
	int p1 = 200;
	ReverseMagicalSource * obj = new ReverseMagicalSource();
	clock_t start = clock();
	int my_answer = obj->find(p0, p1);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p2 = 209;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p2 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p2 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
