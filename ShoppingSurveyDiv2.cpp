class ShoppingSurveyDiv2 {
public:
	int minValue(int, vector <int>);
};


int ShoppingSurveyDiv2::minValue(int N, vector <int> s) {
	int sum=0,i,M=s.size();
	for(i=0;i<M;i++)
	sum+=s[i];
	
	if(N*(M-1)>=sum) return 0;
	
	return sum-N*(M-1);
		
				
}


