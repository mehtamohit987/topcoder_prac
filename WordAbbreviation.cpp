class WordAbbreviation {
public:
	vector <string> getAbbreviations(vector <string>);
};

vector <string> WordAbbreviation::getAbbreviations(vector <string> words) {


	vector<string> v;
	int k;

	for(int i=0;i<words.size();i++)
	{
		k=1;
		for(int j=0;j<words.size();j++)
		{

			if(i!=j)
			{
                //cout<<"entered";
                while(k<words[i].length()&&words[i].substr(0,k)==words[j].substr(0,k))
				k++;

			}
		}

		v.push_back(words[i].substr(0,k));
	}


	return v;
}

