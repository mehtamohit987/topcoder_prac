class SoldierLabeling {
public:
	int count(int, int, int);
};

int SoldierLabeling::count(int n, int lowerBound, int upperBound) {

	if(n<pow(10,lowerBound-1)) return 0;
	
	if(n<pow(10,upperBound))
	return n-pow(10,lowerBound-1)+1;
	else
	return pow(10,upperBound)-pow(10,lowerBound-1);	
}


double test0() {
	int p0 = 100;
	int p1 = 2;
	int p2 = 2;
	SoldierLabeling * obj = new SoldierLabeling();
	clock_t start = clock();
	int my_answer = obj->count(p0, p1, p2);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p3 = 90;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p3 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p3 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
